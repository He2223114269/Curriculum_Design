package Mypackage;
import java.lang.Math;
public class KY6_4 {
    public static void main(String[] args) {
        System.out.println(Math.abs(-5.8)); // 返回一个数的绝对值
        System.out.println(Math.ceil(3.2)); // 返回大于或等于一个数的最小整数
        System.out.println(Math.floor(3.8)); // 返回小于或等于一个数的最大整数
        System.out.println(Math.round(3.8)); // 返回最接近的整数
        System.out.println(Math.round(3.2)); // 返回最接近的整数
        System.out.println(Math.min(3, 2)); // 返回两个数中的最小值
        System.out.println(Math.max(Math.PI, 4)); // 返回两个数中的最大值
        System.out.println(Math.log(7.0)); // 返回一个数的自然对数
        System.out.println(Math.pow(7, 2)); // 返回一个数的幂
        System.out.println(Math.exp(0.4)); // 返回e的指数
        System.out.println("e is:" + Math.E); // 打印自然数e的值
        System.out.println("π is:" + Math.PI); // 打印圆周率π的值
        System.out.println(Math.random()); // 打印一个随机数
    }
}